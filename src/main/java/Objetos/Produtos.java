
package Objetos;


public class Produtos {
     private String nome;
    private String desc;
    private Double valorUni;
    private int qtd;
    private Double valorTo;
    private String fornecedor;
    private Double valorSoma;

    
    public Double getValorSoma() {
        return valorSoma;
    }

    
    public void setValorSoma(Double valorSoma) {
        this.valorSoma = valorSoma;
        
    }
    

    
    public String getNome() {
        return nome;
    }

    
    public void setNome(String nome) {
        this.nome = nome;
    }

    
    public String getDesc() {
        return desc;
    }

    
    public void setDesc(String desc) {
        this.desc = desc;
    }

    
    public Double getValorUni() {
        return valorUni;
    }

    
    public void setValorUni(Double valorUni) {
        this.valorUni = valorUni;
    }

    
    public int getQtd() {
        return qtd;
    }

    
    public void setQtd(int qtd) {
        this.qtd = qtd;
    }

    
    public Double getValorTo() {
        return valorTo;
    }

    
    public void setValorTo(Double valorTo) {
        this.valorTo = valorTo;
    }

    
    public String getFornecedor() {
        return fornecedor;
    }

    
    public void setFornecedor(String fornecedor) {
        this.fornecedor = fornecedor;
    }
    
    
        
    
   
    
}
